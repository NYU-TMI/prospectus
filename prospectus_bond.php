<P align=left><B>Barclays U.S. Aggregate Bond Index</B> is a broad-based, 
market-value-weighted benchmark that measures the performance of the investment 
grade, U.S. dollar-denominated, fixed-rate taxable bond market. Sectors in the 
index include Treasuries, government-related and corporate securities, MBS 
(agency fixed-rate and hybrid ARM pass-throughs), ABS, and CMBS. </P>
<P align=left><B>Barclays U.S. Universal Bond Index</B> represents the union of 
the Barclays U.S. Aggregate Bond Index, the Barclays U.S. Corporate High Yield 
Bond Index, the Barclays 144A Bond Index, the Barclays Eurodollar Bond Index, 
the Barclays U.S. Emerging Markets Bond Index, and the non-ERISA portion of the 
Barclays U.S. CMBS Index. Municipal debt, private placements, and 
non-dollar-denominated issues are excluded from the index. The only constituent 
of the index that includes floating-rate debt is the Barclays U.S. Emerging 
Markets Bond Index. </P>
