<P align=left><B><I>Description of Principal Security Types</I></B> </P>
<P align=left><I>Debt securities</I> are used by issuers to borrow money. The 
issuer usually pays a fixed, variable, or floating rate of interest, and must 
repay the amount borrowed, usually at the maturity of the security. Some debt 
securities, such as zero coupon bonds, do not pay current interest but are sold 
at a discount from their face values. Debt securities include corporate bonds, 
government securities, repurchase agreements, money market securities, mortgage 
and other asset-backed securities, loans and loan participations, and other 
securities believed to have debt-like characteristics, including hybrids and 
synthetic securities. </P>
<P align=left>A <I>repurchase agreement</I> is an agreement to buy a security at 
one price and a simultaneous agreement to sell it back at an agreed-upon price. 
</P>
<P align=left><I>Derivatives</I> are investments whose values are tied to an 
underlying asset, instrument, currency, or index. Derivatives include futures, 
options, forwards, and swaps, such as interest rate swaps (exchanging a floating 
rate for a fixed rate), total return swaps (exchanging a floating rate for the 
total return of an index, security, or other instrument or investment) and 
credit default swaps (buying or selling credit default protection). 
Currency-related derivatives, in particular, include foreign exchange (FX) 
transactions such as spot FX trades, FX forwards, non-deliverable forwards, and 
cross-currency FX trades. </P>
<P align=left><B><I><FONT size=-2>Prospectus</FONT></I></B> </P>
<P align=left><I>Forward-settling securities</I> involve a commitment to 
purchase or sell specific securities when issued, or at a predetermined price or 
