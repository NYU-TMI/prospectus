<H2 align=left><A name=ref236033></A>Principal Investment Risks</H2>
<UL>
  <LI><I>Interest Rate Changes.</I> Interest rate increases can cause the price 
  of a debt security to decrease. 
  <LI><I>Foreign Exposure.</I> Foreign markets, particularly emerging markets, 
  can be more volatile than the U.S. market due to increased risks of adverse 
  issuer, political, regulatory, market, or economic developments and can 
  perform differently from the U.S. market. Foreign exchange rates also can be 
  extremely volatile. 
  <LI><I>Prepayment.</I> The ability of an issuer of a debt security to repay 
  principal prior to a security's maturity can cause greater price volatility if 
  interest rates change. 
  <LI><I>Issuer-Specific Changes.</I> The value of an individual security or 
  particular type of security can be more volatile than, and can perform 
  differently from, the market as a whole. A decline in the&nbsp;credit quality 
  of an issuer or a provider of credit support or a maturity-shortening 
  structure for a security can&nbsp;cause the price of a security to decrease. 
  Lower-quality debt securities (those of less than investment-grade quality, 
  also referred to as high yield debt securities or junk bonds) and certain 
  types of other securities involve greater risk of default or price changes due 
  to changes in the credit quality of the issuer. The value of lower-quality 
  debt securities and certain types of other securities can be more volatile due 
  to&nbsp;increased sensitivity to adverse issuer,&nbsp;political, regulatory, 
  market, or economic developments and can be difficult to resell. </LI></UL>
