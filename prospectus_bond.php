each class of a multiple class fund is treated separately. A roundtrip 
transaction occurs when a shareholder sells fund shares (including exchanges) 
within 30 days of the purchase date. </P>
<P align=left>Shareholders with two or more roundtrip transactions in a single 
fund within a rolling 90-day period will be blocked from making additional 
purchases or exchange purchases of the fund for 85 days. Shareholders with four 
or more roundtrip transactions across all Financial funds within any rolling 
12-month period will be blocked for at least 85 days from additional purchases 
or exchange purchases across all Financial funds. Any roundtrip within 12 months 
of the expiration of a multi-fund block will initiate another multi-fund block. 
Repeat offenders may be subject to long-term or permanent blocks on purchase or 
exchange purchase transactions in any account under the shareholder's control at 
any time. In addition to enforcing these roundtrip limitations, the fund may in 
its discretion restrict, reject, or cancel any purchases or exchanges that, in 
the Adviser's opinion, may be disruptive to the management of the fund or 
otherwise not be in the fund's interests. </P>
