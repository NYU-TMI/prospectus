index fund and its index may vary somewhat due to factors such as fees and 
expenses of the underlying fund, imperfect correlation between the underlying 
fund's securities and those in its index, timing differences associated with 
additions to and deletions from the index, and changes in the shares outstanding 
of the component securities. An underlying index fund may not be fully invested 
at times, either as a result of cash flows into the underlying fund or as a 
result of reserves of cash held by the underlying fund to meet redemptions. The 
use of sampling techniques or futures or other derivative positions may affect 
an underlying index fund's ability to achieve close correlation with its index. 
</P>
<P align=left><I>Leverage Risk.</I> Derivatives and forward-settling securities 
involve leverage because they can provide investment exposure in an amount 
exceeding the initial investment. Leverage can magnify investment risks and 
cause losses to be realized more quickly. A small change in the underlying 
asset, instrument, or index can lead to a significant loss. Assets segregated to 
cover these transactions may decline in value and are not available to meet 
redemptions. Forward-settling securities also involve the risk that a security 
will not be issued, delivered, or paid for when anticipated. Government 
legislation or regulation could affect the use of these transactions and could 
limit a fund's ability to pursue its investment strategies. </P>
<P align=left><I>"Growth" Investing.</I> "Growth" stocks can react differently 
to issuer, political, market, and economic developments than the market as a 
whole and other types of stocks. "Growth" stocks tend to be more expensive 
relative to their earnings or assets compared to other types of stocks. As a 
result, "growth" stocks tend to be sensitive to changes in their earnings and 
more volatile than other types of stocks. </P>
<P align=left><I>"Value" Investing.</I> "Value" stocks can react differently to 
issuer, political, market, and economic developments than the market as a whole 
and other types of stocks. "Value" stocks tend to be inexpensive relative to 
their earnings or assets compared to other types of stocks. However, "value" 
stocks can continue to be inexpensive for long periods of time and may not ever 
realize their full value. </P>
<P align=left><I>Inflation-Protected Debt Exposure.</I> Inflation-protected debt 
securities tend to react to changes in real interest rates. Real interest rates 
represent nominal (stated) interest rates reduced by the expected impact of 
inflation. In general, the price of an inflation-protected debt security can 
fall when real interest rates rise, and can rise when real interest rates fall. 
Interest payments on inflation-protected debt securities can be unpredictable 
and will vary as the principal and/or interest is adjusted for inflation. </P>
<P align=left><I>Small Cap Investing.</I> The value of securities of smaller, 
less well-known issuers can be more volatile than that of larger issuers and can 
react differently to issuer, political, market, and economic developments than 
the market as a whole and other types of stocks. Smaller issuers can have more 
limited product lines, markets, and financial resources. </P>
